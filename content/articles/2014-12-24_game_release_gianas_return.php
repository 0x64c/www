<h2>Giana, get ready!</h2>

<p>Evil Swampy and his followers have stolen the same magic ruby, which once made it possible for Giana and Maria to return from their dream. As if this was not bad enough, Maria was kidnapped as well! Embark on a new quest as Giana, get back the magic ruby and save your beloved sister Maria from the clutches of Swampy!</p>

<p>Enjoy the following features:</p>
<ul>
<li>56 unique levels divided into 7 worlds</li>
<li>Boss battles</li>
<li>Power-ups and bonuses</li>
<li>Hidden Warp Zones and Bonus Caves</li>
<li>Top 10 High-Score list</li>
<li>Hidden Trainer</li>
<li>Old school pixelart and hand drawn backgrounds</li>
<li>Over 18 chiptune style music tracks</li>
<li>Multiple languages: German, English, French, Spanish</li>
</ul>

