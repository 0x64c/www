<h2>The classic returns</h2>
<p>Retroguru<a href=#1>[1]</a> did it again! With the porting help from Zear, another classic joins the ever growing list of game titles available for GCW Zero.</p>
<p>Giana's Return<a href=#2>[2]</a> aims to be a worthy <span class="text_italic">unofficial</span> sequel to "The Great Giana Sisters" - an all-time classic platform game made in the late 80s by Armin Gessert, Manfred Trenz and Chris H&#252;lsbeck. The title back then gained massive attention as a "Super Mario" copy, but still had its very own charm and fan base. Giana's Return expands on its spiritual predecessor, offering a whole new level of experience.</p>

