<p>Neverball expands on the ideas of previous games in the genre, allowing many different types of levels to be created. Some levels focus on balance and finesse. Other levels test puzzle solving ability, using timers, transporters, and switches as challenges. Still others require racing at high speeds across racetracks which might include huge ramps or loops. A final class of levels involve solving labyrinths within a set amount of time. The single uniting factor is its excellent adherence to physics, which really helps to immerse the player regardless.</p>

<p>Neverball & Neverputt are packaged together and can be freely downloaded from the <a href="http://repo.gcw-zero.com">GCW software repository</a>.</p>
