<p>Neverball is two games in one: it includes Neverputt, a miniature golf game with many unique levels and support for 1-4 players, built on the physics and graphics engine of Neverball. For the GCW Zero port, new controls and camera options have been added, as well as a new 'Finesse Mode' allowing for finer movement control in the absence of a mouse. In addition to including the many official levelsets, the port also comes with <a href="http://forum.nevercorner.net/viewtopic.php?id=2793">three excellent levelsets</a> from community map-creator Reshaun Francis. The game's community is quite active and unofficial levelsets can easily be installed. Additional levels can be found at the <a href="http://wiki.nevercorner.net/doku.php/level_sets">wiki page</a> from nevercorner.net, a self proclaimed source for community-driven Neverball/Neverputt information. More level contributions can be found in their official forums.</p>

