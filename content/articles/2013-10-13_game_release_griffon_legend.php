<h1>News :: Game Release - Griffon Legend</h1>
Date: 2013-10-13

<h2>A retro inspired action RPG</h2>

<p>The GCW Zero has received an exclusive port of "The Griffon Legend" (released in 2005) from FreeBasic to C by <a href="http://github.com/dmitrysmagin/griffon_legend">Dmitry Smagin</a>. The game, an action RPG, features an original storyline and is inspired by the 16-bit style RPGs of the SNES/GENESIS era. It was originally written by <a href="http://syn9.thehideoutgames.com/index_backup.php">Daniel "Syn9" Kennedy</a> in FreeBASIC, with sound by David Turner and was limited to a Windows release. With the help of Dmitry Smagin the game is now featured on the GCW Zero for the first time ever. Griffon Legend can be freely downloaded from the GCW <a href="http://www.gcw-zero.com/downloads">software repository</a> and will be preinstalled on new units.

