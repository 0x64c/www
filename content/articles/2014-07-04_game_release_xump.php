<h1>News :: Game Release - Xump</h1>
Date: 2014-07-04, Authors: Albert H. &amp; Surkow

<h2>Space Cleaning Madness</h2>

<p>We are proud to announce the release of the GCW Zero version of Xump created by Retroguru<a href=#1>[1]</a> (of Sqrxz fame<a href=#2>[2]</a>) and ported by Zear. This game, a time-trial puzzler, features a retro 16-bit theme combined with a pumping chiptune soundtrack and just about everything else we've come to expect from the fine lads at Retroguru.</p>

