<p>You play the titular character, the robot companion of a boy named Holger, who must clear out the space minefields by moving Xump along the collapsing paths. Along the way, you'll encounter left-over mines and arrow plates which guide Xump in different directions, adding to the frustration of clearing space minefields. In this race against the clock, Xump and Holger really have their work cut out for them.</p>

<p>Initially starting development in 2005 under Psilocybin Development, the game has been completely revamped by the Retroguru team. They've made entirely new levels, graphics and music, to name a few things. The game is now featured on the GCW Zero for the first time ever. Xump can be freely downloaded from the GCW software repository<a href=#3>[3]</a>.</p>

