<p>Some of the identifying features of the Dish Analog Cap:</p>
<ul>
<li>The diameter of the analog cap has been increased for a larger, more comfortable experience.</li>
<li>The surface is concave, opposite of the convex stock GCW Zero analog cap.</li>
<li>The uninterrupted smooth surface provides more comfort for a larger finger size and provides plenty of surface area for your thumb.</li>
<li>The thickness coming out of the console has been reduced compared to the other two new Analog Caps making it the most streamlined and low profile of the three.</li>
</ul>

<h2>Start/Select buttons</h2>

<p>The new GCW Zero Start and Select buttons have been completely redesigned by our Industrial Design team to be able to offer a more comfortable menu launching experience, less rattling, and more color options.</p>

<ul class="gallery2" style="width: 660px;">
<li><img src="images/articles/17/P1000808_small.jpg"><p>Side view of the new Start/Select buttons shown in white.</p></li>
<li><img src="images/articles/17/P1000821_small.jpg"><p>Top view showing a comparison between the original Start/Select buttons and its 3D printed counterpart which can act as both a Start and Select button.</p></li>
</ul>

<p>Here are some specific improvements that we made to the Start and Select buttons:</p>

<ul>
<li>The curvature of the buttons has been increased making it easier and more comfortable to press both buttons down for easy menu access.</li>
<li>The thickness of the contact disk which hold the buttons in place has been increased to fill up the extra space and reduce the rattling of the Start and Select buttons.</li>
<li>Each button is designed to be modular, meaning that each button can now be swapped into either Start or Select button location, allowing for color personalization.</li>
<li>The outer edges of the Start and Select keypad contact disk have been rounded.</li>
<li>The buttons are solid instead of hollow giving them a quality feel.</li>
<li>The very bottom of the buttons which contacts the microswitches has been changed from a hollow cross shape to a solid square flat base which more equally distributes the force pressing down on the microswitch and also makes it feel more solid.</li>
</ul>

<h2>Installing the Parts</h2>

<!-- HTML Comment: duplicate of old article --->
<p>Preparing the parts for installation is easy, all that needs to be done is to trim off the thin piece of plastic joining the two parts with a craft knife or scissors and you are good to go.</p>

<p>To achieve different colored parts, Shapeways prints the parts white and then dyes them another color in controlled batches. The color of the dyed parts will hold up during normal use, but if you sand or cut the parts, the white plastic will show through. This is why the two connected buttons are connected on the back faces of each button. When trimmed, the white patch of plastic will not show after installation.</p>

<p>The Start and Select buttons will require you to open your console during installation, but the new Analog Caps will not! All you need to do is gently wiggle the stock GCW Analog Cap, and pull straight upwards to remove it. When installing the new Analog Caps, you may notice that fit is tighter at first when you press them down onto the analog stick. If you're trying multiple new Analog caps, take care when removing the new installed caps. This task might be easier by using a flat plastic gift card or guitar pick under two opposite sides to provide upward pressure to remove the cap.</p> 

<p>Because replacing the buttons is definitely a task for the DIY folks, we thought it would be fun to give a few ideas of what you could do with this leftover second part! Think of it as your own DIY copy of the button to test a custom paint job, sand the surface to create custom button curvature, or Dremel in some grooves for grip. It's for you to experiment and have some fun! We look forward to seeing all your cool DIY creations. You can share your custom creations with us on the <a href="http://boards.dingoonity.org/index.php#c10" target="_blank">Dingoonity</a> forums, <a href="https://twitter.com/gcwzero" target="_blank">Twitter @gcwzero</a>, and on our <a href="https://www.facebook.com/pages/GCW-Zero/518785154808241" target="_blank">Facebook</a> page!</p>

