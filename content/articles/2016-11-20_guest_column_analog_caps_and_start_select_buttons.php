<p>The Scoop Cap has the following features:</p>
<ul>
<li>The diameter of the analog cap has been increased for a larger, more comfortable experience.</li>
<li>The surface is concave, opposite of the convex stock GCW Zero analog cap.</li>
<li>A smooth, raised outer ring provides additional grip.</li>
</ul>

<h3>Dish Cap</h3>

<p>Just like with the Scoop Analog Cap, the Dish Cap has a concave surface, but offers a more comfortable experience for people who prefer a larger Analog Cap.</p>

<ul class="gallery2" style="width: 660px;">
