<p>Just like with the 3D printed DPAD and ABXY buttons for the GCW Zero, each of the newly introduced parts consists of two pieces which are connected together by a thin piece of plastic forming one actual part. As explained in the article <a href="news.php?id=16">introducing the redesigned DPAD and ABXY buttons</a>, Shapeways does not currently allow us to specify a print orientation for our parts. This could result in a single part being printed using the wrong orientation with a tangible stepping or ridging effect on the surface of the part. By joining two parts together in different orientations we can guarantee at least one part will be printed correctly.</p>

<h2>Analog Stick Cap variations</h2>

<p>Instead of offering a one-size-fits-all approach we decided to provide different customization options for the analog stick. Each of these can be installed and swapped without ever needing to open up your GCW Zero console!</p>

