<p>Reproducing the fruit pattern as seen on the right-hand side of the playfield will help Sheran to make his way through the dense underbelly of the jungle. Sounds easy enough?</p>

<ul class="gallery2" style="width: 660px;">
<li><img src="images/articles/fruity_howtoplay.gif"><p>The in-game howto</p></li>
<li><img src="images/articles/longblendgiffruity3.gif"><p>Morphing selected fruits</p></li>
