<p>By using the D-pad to move your cursor you can select a specific fruit to morph. When read from top to bottom, the middle of the screen shows in what order the fruits transform into other fruits.
The center of your selection morphs by two steps, while the four surrounding fruits change by one step into other fruits.</p>

<p>Like all the other releases by Retroguru, Fruit'Y can be freely downloaded from the <a href="http://repo.gcw-zero.com">GCW Software Repository</a>.</p>
