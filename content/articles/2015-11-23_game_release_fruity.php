<h2>Playing with edibles</h2>

<p>Tiger mum warns little Sheran not to eat the jungle fruit, but Sheran is all too tempted by the allure of that delectable canopy vegetation. As long as you're here, you can help Sheran to safely traverse the jungle in his quest to sample that sweet & juicy jungle fruit.</p>

